package Inheritance;

public class Laptop 
{

	private int noOfUSBPost;
	private int processorSpeed;
	
	public void setNoOfUSBPost(int noOfUSB)
	{
		this.noOfUSBPost=noOfUSB;
	}
	public int getNoOfUSBPost()
	{
		return noOfUSBPost;
	}
	
	public void setProcessorSpeed(int pro_Speed)
	{
		this.processorSpeed=pro_Speed;
	}
	public int getProcessorSpeed()
	{
		return processorSpeed;
	}
	
	
}
